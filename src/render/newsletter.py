def _section_open(title: str) -> str:
    return f'<div class="section"><div class="section-title">{title}</div>'


def _section_close() -> str:
    return '</div>'


def _day_header(d: date) -> str:
    return f'<div class="day-header">{_fmt_date(d)}</div>'


def _league_header(label: str, round_info: str) -> str:
    round_html = f'<span class="league-round">— {round_info}</span>' if round_info else ""
    return f'<div class="league-header">{label} {round_html}</div>'


def _render_schedule_event(ev: SportEvent, channels: List[str]) -> str:
    time_str = _fmt_time(ev.datetime_utc)
    teams = f"{ev.home_team}  vs  {ev.away_team}"
    ch_html = " ".join(_channel_badge(ch) for ch in channels) if channels else ""
    highlight = _highlight_star() if ev.is_highlight else ""
    return (
        f'<div class="event-row">'
        f'<span class="event-time">{time_str}</span>'
        f'<span class="event-teams">{teams}{highlight}</span>'
        f'<span class="event-channels">{ch_html}</span>'
        f'</div>'
    )


def _render_highlight_event(ev: SportEvent, channels: List[str]) -> str:
    time_str = _fmt_time(ev.datetime_utc)
    d = _local_date(ev.datetime_utc)
    date_str = _fmt_date(d)
    ch_html = " ".join(_channel_badge(ch) for ch in channels) if channels else ""
    round_html = f" — {ev.round_info}" if ev.round_info else ""
    return (
        f'<div class="highlight-card">'
        f'<div class="hl-teams">{ev.emoji} {ev.home_team}  vs  {ev.away_team}</div>'
        f'<div class="hl-info">{date_str}, {time_str}{round_html} {ch_html}</div>'
        f'</div>'
    )


def _render_result_event(ev: SportEvent) -> str:
    if ev.home_score is None:
        return ""
    score = f"{ev.home_team} {ev.home_score} - {ev.away_score} {ev.away_team}"
    matched = ev.matched_team.lower() if ev.matched_team else ""
    home_is_fav = matched and matched in ev.home_team.lower()
    away_is_fav = matched and matched in ev.away_team.lower()

    if home_is_fav:
        won = ev.home_score > ev.away_score
    elif away_is_fav:
        won = ev.away_score > ev.home_score
    else:
        won = None

    if won is True:
        css = "result-row result-win"
        icon = "✅"
    elif won is False:
        css = "result-row result-lose"
        icon = "❌"
    else:
        css = "result-row"
        icon = "•"

    return f'<div class="{css}">{icon} {score}</div>'


def _render_cycling_event(ce: CyclingEvent, current_day: date) -> str:
    ch_html = " ".join(_channel_badge(ch) for ch in ce.channels_fr)
    loc = f" — {ce.location}" if ce.location else ""
    label = f"{ce.name}{loc}"
    return (
        f'<div class="event-row">'
        f'<span class="event-time">—</span>'
        f'<span class="event-teams">{ce.emoji} {label}</span>'
        f'<span class="event-channels">{ch_html}</span>'
        f'</div>'
    )


def _render_cycling_multi_day(ce: CyclingEvent) -> str:
    ch_html = " ".join(_channel_badge(ch) for ch in ce.channels_fr)
    loc = f" — {ce.location}" if ce.location else ""
    dates = f"{_fmt_date(ce.start_date)} → {_fmt_date(ce.end_date)}"
    label = f"{ce.name}{loc}"
    return (
        f'<div class="event-row">'
        f'<span class="event-time">—</span>'
        f'<span class="event-teams">{ce.emoji} {label}'
        f'<br><span style="font-size:11px;color:#555;">{dates}</span></span>'
        f'<span class="event-channels">{ch_html}</span>'
        f'</div>'
    )


def _render_cycling_result(ce: CyclingEvent) -> str:
    loc = f" — {ce.location}" if ce.location else ""
    return (
        f'<div class="result-row">'
        f'{ce.emoji} {ce.name}{loc}'
        f'</div>'
    )
