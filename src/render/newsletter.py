# ── Main render function ────────────────────────────────────

def render_newsletter(
    upcoming_events: List[SportEvent],
    past_events: List[SportEvent],
    cycling_upcoming: List[CyclingEvent],
    cycling_past: List[CyclingEvent],
    channels_map: Dict[str, List[str]],
    week_start: date,
    week_end: date,
    prev_week_start: date,
    prev_week_end: date,
) -> Tuple[str, str]:
    """
    Render the full HTML newsletter.
    Returns (html_body, subject_line).
    """
    if LANGUAGE == "fr":
        title = "AU MENU CETTE SEMAINE"
        week_label = f"Semaine du {week_start.day} au {week_end.day} {MONTHS_FR[week_end.month]} {week_end.year}"
        highlights_title = "🌟 GRANDES RENCONTRES À NE PAS RATER"
        schedule_title = "📅 PROGRAMME JOUR PAR JOUR"
        results_title = "📊 RÉSULTATS DE LA SEMAINE PASSÉE"
        no_events = "Aucun événement cette semaine pour vos favoris."
        no_results = "Aucun résultat la semaine passée."
        cycling_title = "🚴 ÉVÉNEMENTS CYCLISME"
        footer_text = "Généré automatiquement par Télé7Sport"
        subject = f"📺 Télé7Sport — {week_label}"
    else:
        title = "THIS WEEK'S MENU"
        week_label = f"Week of {week_start.strftime('%b %d')} — {week_end.strftime('%b %d, %Y')}"
        highlights_title = "🌟 MUST-WATCH EVENTS"
        schedule_title = "📅 DAY BY DAY SCHEDULE"
        results_title = "📊 LAST WEEK'S RESULTS"
        no_events = "No events this week for your favorites."
        no_results = "No results from last week."
        cycling_title = "🚴 CYCLING EVENTS"
        footer_text = "Automatically generated by Télé7Sport"
        subject = f"📺 Télé7Sport — {week_label}"

    # ── Group upcoming events by day ────────────────────────
    events_by_day: Dict[date, List[SportEvent]] = {}
    for ev in upcoming_events:
        d = _local_date(ev.datetime_utc)
        events_by_day.setdefault(d, []).append(ev)

    # ── Group past events by league ─────────────────────────
    results_by_league: Dict[str, List[SportEvent]] = {}
    for ev in past_events:
        if ev.status == "completed":
            results_by_league.setdefault(ev.league_label, []).append(ev)

    # ── Highlights ──────────────────────────────────────────
    highlight_events = [ev for ev in upcoming_events if ev.is_highlight]

    # ── Start building HTML ─────────────────────────────────
    html_parts = [_html_head(title, week_label)]

    # Highlights section
    if highlight_events:
        html_parts.append(_section_open(highlights_title))
        for ev in highlight_events:
            ch_key = f"{ev.league_id}:{ev.home_team}:{ev.away_team}"
            channels = channels_map.get(ch_key, [])
            html_parts.append(_render_highlight_event(ev, channels))
        html_parts.append(_section_close())

    # Day-by-day schedule
    html_parts.append(_section_open(schedule_title))
    if not events_by_day and not cycling_upcoming:
        html_parts.append(f'<p style="color:#888;font-style:italic;">{no_events}</p>')
    else:
        current = week_start
        while current <= week_end:
            day_events = events_by_day.get(current, [])
            day_cycling = [c for c in cycling_upcoming if c.start_date <= current <= c.end_date]
            if day_events or day_cycling:
                html_parts.append(_day_header(current))
                # Group day events by league
                by_league: Dict[str, List[SportEvent]] = {}
                for ev in day_events:
                    by_league.setdefault(f"{ev.emoji} {ev.league_label}", []).append(ev)
                for league_header, evs in by_league.items():
                    html_parts.append(_league_header(league_header, evs[0].round_info))
                    for ev in evs:
                        ch_key = f"{ev.league_id}:{ev.home_team}:{ev.away_team}"
                        channels = channels_map.get(ch_key, [])
                        html_parts.append(_render_schedule_event(ev, channels))
                # Cycling for this day — grouped by league
                cycling_by_league: Dict[str, List[CyclingEvent]] = {}
                for ce in day_cycling:
                    cycling_by_league.setdefault(f"{ce.emoji} {ce.league_label}", []).append(ce)
                for league_hdr, ces in cycling_by_league.items():
                    html_parts.append(_league_header(league_hdr, ""))
                    for ce in ces:
                        html_parts.append(_render_cycling_event(ce, current))
            current += timedelta(days=1)
    html_parts.append(_section_close())

    # Cycling standalone section (multi-day events)
    standalone_cycling = [c for c in cycling_upcoming if c.end_date > c.start_date]
    if standalone_cycling:
        html_parts.append(_section_open(cycling_title))
        for ce in standalone_cycling:
            html_parts.append(_render_cycling_multi_day(ce))
        html_parts.append(_section_close())

    # Past results
    if results_by_league or cycling_past:
        html_parts.append(_section_open(results_title))
        for league_label, evs in results_by_league.items():
            emoji = evs[0].emoji if evs else ""
            html_parts.append(_league_header(f"{emoji} {league_label}", ""))
            for ev in evs:
                html_parts.append(_render_result_event(ev))
        for ce in cycling_past:
            html_parts.append(_render_cycling_result(ce))
        html_parts.append(_section_close())
    elif not results_by_league:
        html_parts.append(_section_open(results_title))
        html_parts.append(f'<p style="color:#888;font-style:italic;">{no_results}</p>')
        html_parts.append(_section_close())

    html_parts.append(_html_footer(footer_text))
    return "\n".join(html_parts), subject


