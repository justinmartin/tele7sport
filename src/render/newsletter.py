TZ = pytz.timezone(TIMEZONE)


def _fmt_date_fr(d: date) -> str:
    return f"{DAYS_FR[d.weekday()]} {d.day} {MONTHS_FR[d.month]}"


def _fmt_date_en(d: date) -> str:
    return d.strftime("%A %B %d")


def _fmt_date(d: date) -> str:
    return _fmt_date_fr(d) if LANGUAGE == "fr" else _fmt_date_en(d)


def _fmt_time(dt_utc: datetime) -> str:
    local = dt_utc.astimezone(TZ)
    return local.strftime("%H:%M")


def _local_date(dt_utc: datetime) -> date:
    return dt_utc.astimezone(TZ).date()


